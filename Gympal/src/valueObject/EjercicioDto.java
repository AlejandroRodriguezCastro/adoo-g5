package valueObject;

public class EjercicioDto {
	
	private int nroEjercicio;
	private int series;
	private int repeticiones;
	private float pesoAsignado;
	public int getSeries() {
		return series;
	}
	public void setSeries(int series) {
		this.series = series;
	}
	public int getRepeticiones() {
		return repeticiones;
	}
	public void setRepeticiones(int repeticiones) {
		this.repeticiones = repeticiones;
	}
	public float getPesoAsignado() {
		return pesoAsignado;
	}
	public void setPesoAsignado(float pesoAsignado) {
		this.pesoAsignado = pesoAsignado;
	}
	public int getNroEjercicio() {
		return nroEjercicio;
	}
	public void setNroEjercicio(int nroEjercicio) {
		this.nroEjercicio = nroEjercicio;
	}
	
	

}
